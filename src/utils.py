    @staticmethod
    def read_states():
        with open('datafolder//columns.txt', 'r') as f:
            raw = f.read()
            contents = raw[:-1]  # to remove last space
            return contents.split('_')

    @classmethod
    def drop_cols(cls, df):

        states = cls.read_states()
        return df[states]
